package main

import (
	"fmt"
	"strings"
)

type hand struct {
	primary    string
	encrypted  string
	pointValue int
}

func (h hand) pointsAgainst(h2 hand) int {

	if h == h2 {
		return drawPoints + h.pointValue
	}

	// rock beats scissors
	if h == rock && h2 == scissors {
		return winPoints + h.pointValue
	}

	// paper beats rock
	if h == paper && h2 == rock {
		return winPoints + h.pointValue
	}

	// scissors beats paper
	if h == scissors && h2 == paper {
		return winPoints + h.pointValue
	}

	return h.pointValue
}

func (h hand) pointsIfOutcome(o outcome) int {
	switch o {
	case win:
		switch h {
		case rock:
			return paper.pointsAgainst(h)
		case paper:
			return scissors.pointsAgainst(h)
		case scissors:
			return rock.pointsAgainst(h)
		}
	case lose:
		switch h {
		case rock:
			return scissors.pointsAgainst(h)
		case paper:
			return rock.pointsAgainst(h)
		case scissors:
			return paper.pointsAgainst(h)
		}
	case draw:
		return h.pointsAgainst(h)
	}

	panic(fmt.Sprintf("wut: outcome=%v, hand=%v", o, h))
}

var (
	rock       = hand{"A", "X", 1}
	paper      = hand{"B", "Y", 2}
	scissors   = hand{"C", "Z", 3}
	handLookup = map[string]hand{}
)

type outcome string

const (
	winPoints  = 6
	drawPoints = 3
	losePoints = 0

	win  outcome = "Z"
	lose outcome = "X"
	draw outcome = "Y"
)

func main() {
	for _, h := range []hand{rock, paper, scissors} {
		handLookup[h.primary] = h
		handLookup[h.encrypted] = h
	}

	var games = strings.Split(input, "\n")
	var pointsAgainstScore int
	var outcomeBasedScore int
	for _, game := range games {
		moves := strings.Split(game, " ")
		opp := moves[0]
		resp := moves[1]

		pointsAgainstScore += handLookup[resp].pointsAgainst(handLookup[opp])
		outcomeBasedScore += handLookup[opp].pointsIfOutcome(outcome(resp))
	}

	fmt.Printf("Total points if against: %d\n", pointsAgainstScore)
	fmt.Printf("Total point against outcomes %d\n", outcomeBasedScore)
}

